function handleFormSubmit(e) {
    e.preventDefault();

    const formData = new FormData(e.target);
    const name = formData.get('name').trim();
    const email = formData.get('email').trim();
    const message = formData.get('message').trim();

    // Validation
    const errors = validateForm(name, email, message);
    if (errors.length > 0) {
        showNotification(errors.join('\n'), 'error');
        return;
    }

    // Simulate form submission
    const submitBtn = e.target.querySelector('button[type="submit"]');
    const originalText = submitBtn.textContent;

    submitBtn.disabled = true;
    submitBtn.innerHTML = '<span class="loading"></span> ' +
        (currentLang === 'ar' ? 'جاري الإرسال...' : 'Sending...');

    // Simulate API call
    setTimeout(() => {
        submitBtn.disabled = false;
        submitBtn.textContent = originalText;

        // Reset form
        e.target.reset();

        // Show success message
        showNotification(translations[currentLang].messageSent, 'success');

        // In a real application, you would send the data to a server
        console.log('Form submitted:', { name, email, message });
    }, 3000);
}

function validateForm(name, email, message) {
    const errors = [];

    if (!name) {
        errors.push(translations[currentLang].nameRequired);
    }

    if (!email) {
        errors.push(translations[currentLang].emailRequired);
    } else if (!isValidEmail(email)) {
        errors.push(translations[currentLang].emailInvalid);
    }

    if (!message) {
        errors.push(translations[currentLang].messageRequired);
    }

    return errors;
}

function isValidEmail(email) {
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return emailRegex.test(email);
}

